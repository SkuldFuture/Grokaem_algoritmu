# GROCERY LIST
book = dict()

book["apple"] = 0.67
book["milk"] = 1.49
book["avocado"] = 1.49

print(book)  # {'apple': 0.67, 'milk': 1.49, 'avocado': 1.49}
print(book["avocado"])  # 1.49

# REFERENCE BOOK
phone_book = {}

phone_book["jenny"] = 8675309
phone_book["emergency"] = 911

print(phone_book)   # {'jenny': 8675309, 'emergency': 911}
print(phone_book["jenny"])  # 8675309